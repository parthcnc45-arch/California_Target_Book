                        <section id="Apportionment">
                            <h2 align='center'>The 'Apportionment File' Deadline</h2>
                            <div class='row'>
                                <div class='panel'>
<p>The first of these deadlines was December 31, 2020. That is the &ldquo;Apportionment Deadline.&rdquo; This is the date set in federal law for the Census Bureau to deliver to the President a full count of all the people in the United States, so that Congress can apportion the 535 congressional districts among the states.</p>

<p>However, the Census Bureau determined as early as last May that this was not a viable date for the completion of the overall population count. The Trump Administration had asked the Bureau to separate out undocumented immigrants as the Administration not wanting them counted for apportionment purposes. This did not happen and was likely impossible as there was no citizenship/legal resident question on the census form. Due to these delays the December 31 date came and went with no apportionment file.</p>

<p>Last spring, the Bureau asked Congress to extend the Apportionment Deadline to April 30, 2021, but this was never acted upon. On February 2, 2021, acting Census Bureau Director, Dr. Ron Jarmin issued a lengthy statement on the multiple delays the census faced in 2020 and said that April 30, 2021 is the anticipated deadline for the release of the apportionment file to Congress and the states.</p>
                                </div>
                            </div>  
                        </section>


                        <section id="RedistFile">
                            <h2 align='center'>The 'Redistricting File' Deadline</h2>
                            <div class='row'>
                                <div class='panel'>
<p>The final action of the census in preparation for the 2021 redistricting will be the production of the Census Redistricting File, also known as the Public Law 94-171 file. This law requires that the Census Bureau send to each state a file of the census data in a form that can be used to create election districts. This means taking the raw data developed for the Apportionment File and aggregating it by county, city, unincorporated areas and &ldquo;census blocks&rdquo; for each state. A &ldquo;census block&rdquo; is a city block in built up areas; and in rural areas a geographic unit of 100 to 200 people. These blocks are aggregated into census tracts which are the basic building blocks of districting maps.</p>

<p>Current law allows three months between the time of the apportionment file and the Public Law 94-171 file, so current law sets the deadline for this file as March 31, 2021. However, since the Census now says that it will need until April 30, 2021 to prepare the apportionment file, it is obvious that deadline cannot be met.</p>

<p>On February 12, however, the Bureau shocked the redistricting world by announcing that the redistricting file would not be available until September 30, a six-month delay. In its statement the Bureau said, &ldquo;COVID-19-related delays and prioritizing the delivery of the apportionment results delayed the Census Bureau&rsquo;s original plan to deliver the redistricting data to the states by March 31, 2021.&rdquo;&nbsp;</p>

<p>The Bureau did not explain why it would take six months rather than three months to prepare this file once the Apportionment File was released.&nbsp; But the Bureau did note that states would be ready to redistrict on September 30.&nbsp; &ldquo;Since 2019, states have had access to prototype geographic support products and data tabulations from the 2018 Census Test to help them begin to design their redistricting systems. This is one tool states can use to help minimize the impact of schedule delays. In addition, the Census Bureau today completed the release of all states&rsquo; 2020 Census geographic products needed for redistricting. This will enable states to redistrict promptly upon receipt of their 2020 Census tabulation data.&rdquo;</p>

<p>Because of concerns over the lateness of the PL 94-14 file, the Bureau has also announced that it will be releasing a 'legacy' file in mid to late August.  Although this file will not be user friendly, the Statewide Database does believe it can use this file to begin processing the necessary data to begin redistricting.  So it could be possible to begin the actual line drawing earlier than the estimated November timeline, but whether this will be the case remains very much up in the air.</p>
<p>California does present a special problem. Upon the receipt of the PL 94-171 file, it must be transformed into a useable redistricting database. The Statewide Database, state's redistricting database, is maintained by the University of California. In 2011, the Public law 94-171 file was received on March 8, 2011. The redistricting database was ready for line drawing on April 13, 2011.
</p>

<p>Therefore, it can be assumed it may take up to five weeks for the Public Law 94-171 file to be merged into the state database. The state database contains the population of each precinct in the state. The Census file is just raw population. To redraw the lines and meet the federal standards, it is necessary to know how many voters there are in a district as well as demographic ethnic information, not just how many people there are; therefore, the new Census numbers must be integrated into the state database.</p>

<p>A further delay may be required due to a new law that directs that prison populations be reassigned to the counties from which the prisoners came. The director of the statewide database estimates that could take a month, although that timeline is unclear.</p>

<p>When all this preliminary work is complete, the California Citizens Redistricting Commission can then begin its work of drawing the new district lines.</p>
