                            <h2 align='center'>The Census: Why Do We Take It?</h2>
                            <div class='row'>
                                <div class='panel'>
<p>Since 1790, a full census of the United States has been conducted every ten years. All persons residing in the United States regardless of citizenship or legal status must be counted. The original and still primary reason for the census is to apportion the 535 members of the United States House of Representatives among the states, and thus to determine the number of Electoral Votes each state will receive in presidential elections for the coming decade. Thanks to the &ldquo;one person-one vote&rdquo; Supreme Court rulings, the census determines the make-up of legislative districts as well. In recent years, the census has also been used to apportion more than $1.5 trillion in federal spending among the states.</p>

<p>The United States Constitution requires this census be taken every ten years and federal law has established April 1 of the year ending in &ldquo;zero&rdquo; as census day, meaning the count is of people residing in the United States as of that day. Until the mid-20<sup>th</sup> century the census was conducted by enumerators who travelled door to door listing people and households. In recent decades, the U.S. Census Bureau has mailed a census form to every household in the United States. This year the Bureau developed a simple census form that could be completed online. More than 60 percent of the households in the United States replied to the census using mails or the internet.</p>

<p>Following the mailed forms, the Census Bureau conducts field operations using individual enumerator interviews to count people who have not replied. This was significantly delayed in 2020 due to the pandemic.</p>

<p>The initial schedule for the in-person interviews was to run from April to July 31, 2020. The Bureau could not meet that schedule and delayed completion of the in-person interviews for several months. This delay had a tremendous impact on completion of the full census and on the redistricting process, because this delay resulted in pushing back all the other census deadlines.&nbsp; While the full redistricting process must be completed in time for primary elections in 2022, each time the census deadlines were delayed the redistricting process was also delayed.</p>


                                </div>
                            </div>  
