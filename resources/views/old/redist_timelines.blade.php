                                <h2 align='center'>History of the Commission</h2>
                                <div class='row'>
                                    <div class='panel'>
<p>At the November 2008 election the voters enacted Proposition 11, which was titled the &ldquo;Voters FIRST Act.&rdquo; This measure took redistricting away from the legislature and gave it to a 14-member California Citizens Redistricting Commission. Eight of the members were to be selected via a random sample process overseen by the State Auditor. These eight would then select the final six members. Partisan balance was assured in selection of the commissioners.</p>

<p>The Commission was to come into effect on January 1, 2011, and was to adopt a final plan for the legislature and Board of Equalization by September 15, 2011. However, at the November 2010 election, the voters enacted Proposition 20, titled the &ldquo;Voters FIRST Act for Congress&rdquo;. This measure amended Proposition 11 to include drawing of congressional districts as well as legislative districts, and also reset the deadline for final maps as August 15, 2011 (California Constitution, Article XXI, Section 2 (g).</p>

<p>The ballot measure also amended the Government Code requiring that the Commission display preliminary and final maps for six weeks prior to its vote. In 2013, more detail on how the public display was to proceed was added to the Government Code:</p>

<p>&ldquo;Public comment shall be taken for at least 14 days from the date of public display of the first preliminary statewide maps of the congressional, State Senatorial, Assembly, and State Board of Equalization districts, which shall be publicly displayed no later than July 1 in each year ending in the number one. The commission shall not display any other map for public comment during the 14-day period&hellip;. Public comment shall be taken for at least seven days from the date of public display of any subsequent preliminary statewide maps and for at least three days from the date of public display of any final statewide maps.&rdquo; (Government Code Section 8253 (a) (7)).</p>

<p>The Constitution makes it clear that the Commission&rsquo;s plans are subject to a referendum, and that if the Commission were to fail to adopt any map by August 15, or if a map were rejected in a referendum election, the Supreme Court was instructed to appoint Court Masters to adopt the districts for the coming decade.</p>

<p>The 2011 Commission did enact the four plans as required on August 15, 2011. The State Senate plan was subjected to a referendum, and the referendum was rejected by the voters. So the Commission&rsquo;s plans were in effect for the past decade.</p>

                                    </div>
                                </div>  
                            </section>


                            <section id="Padilla">

                                <h2 align='center'>Legislature v. Padilla</h2>
                                <div class='row'>
                                    <div class='panel'>
                                        <p>In early 2020, it became clear that the census delays meant that the constitutional and statutory deadlines for California&rsquo;s 2021 redistricting could not be met. As a consequence, the legislature filed a lawsuit directly in the California Supreme Court on June 3, 2020, <em>(Legislature of the State of California v Alex Padilla as Secretary of State</em>) asking that the Court order a delay in the production of the final maps until December 31, 2021. Secretary Padilla filed a response largely agreeing with the legislature. Former Governor Arnold Schwarzenegger, Common Cause and the League of Woman Voters, who had sponsored Propositions 11 and 20, filed amici curiae briefs supporting the date changes.</p>

<p>The lawsuit asked the Court to change the date of the preliminary maps from July 1, 2021 to November 1, 2011, and the date for the Secretary of State to accept the final maps from August 15, 2021 to December 15, 2021.</p>

<p>The lawsuit did seem to recognize that the Court faced a difficulty in that the July 1 date for preliminary maps is statutory and could easily be waived, but the August 15, 2021 date is constitutional. As a result, the suit simply calls for the Court to order that maps may be accepted by the Secretary of State until December 15, 2021.</p>

<p>The suit also noted the intent of the legislature to change the 2022 primary election date from March to June 7, 2022, which has been done. The primary election date was changed from June to March for the 2020 primary so California could participate in Super Tuesday.</p>

<p>On July 17, the Court issued an opinion granting the four-month delay. The Court did this without a formal hearing or the normal briefings, and may have acted with urgency because the legislative deadline to place a constitutional amendment on the November 2020 ballot was July 26. The Court thus relieved the legislature of the need to go back to the people to affect the date change.</p>

<p>The Court noted that it has in the past used its power to &ldquo;reform statutes&rdquo; when their language presents an impossible situation. It acknowledged that the Census Bureau said its files will be months late, thus the July 1, 2021 date for displaying preliminary maps cannot be met. Having &ldquo;reformed&rdquo; the preliminary display statute, the Court said it could see no reason not to also waive the August 15 deadline. The Court also noted that if the Commission itself failed to produce the redistricting maps, the Court would have to appoint a special master to conduct the redistricting. However, it appears the Court has no appetite for this.</p>

<p>There is no practical problem with conducting a June 7, 2022 primary if the final maps are known by December 2021. In the 1971 and 1981 redistrictings, the final maps were not established until December and January. In 1991, Supreme Court Masters drew the districts in the fall of 1991. The June 2022 primary can be conducted as long as the final maps are known before the end of 2021.</p>

<p>The case assumed that the redistricting file would be available by July 31. But with the Census indicating that the file could be delayed until September 30, actual drawing of districts may not be able to begin before November. Additionally, the Commission&rsquo;s staff, upon learning that the redistricting file may not be ready until September 30, has indicated that even the December 31 date may not be realistic, given the necessity of public display and public testimony on the draft maps. The staff has said that it may take the Commission until February 15, 2022 to complete its work.</p>

<p>The Commission pointed to language in the Court opinion allowing longer delays in producing the final maps. &ldquo;In the event of further federal delay, we conclude the relevant state deadlines should be shifted accordingly, for the reasons outlined here. Thus, while we today grant a minimum four-month adjustment to the relevant deadlines, we also order that the deadlines be further extended by the length of any additional delay in release of the federal census data beyond four months.&rdquo;</p>

<p>If there are further delays, the Court invited the parties to ask for additional relief.</p>

<p>In addition to the problems outlined above, there is a potential constitutional problem. All Commission maps are subject to referendum in the same manner as a statute is subject to referendum. (California Constitution, Article XXI, Section 2 (i)). The Commission has the sole legal authority to defend its maps. But a February 2022, or even a December 2021, deadline for producing the maps makes a referendum impossible if the primary is in June, and makes a legal challenge problematic. The Court did not deal with issues relating to timing of legal challenges or a referendum in its opinion.</p>

<p>While the Court was asked to act well in advance of these problems occurring, this case may well not be the final word on deadlines. If legal or practical difficulties force line drawing into 2022, there could be need to further delay the 2022 primary.</p>
